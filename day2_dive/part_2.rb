class DiveMovement
  class Orientation < Struct.new(:horiz, :depth, :aim); end

  class Motion < Struct.new(:h_direction, :aim_direction, keyword_init: true)
    def new_orientation(original, value)
      new_aim = original.aim + (value * aim_direction.to_i)
      depth_diff = (value * new_aim) unless h_direction.nil?

      Orientation.new(
        original.horiz + (value * h_direction.to_i),
        original.depth + depth_diff.to_i,
        new_aim
      )
    end
  end

  MOTIONS = {
    "forward" => Motion.new(h_direction: 1),
    "down" => Motion.new(aim_direction: 1),
    "up" => Motion.new(aim_direction: -1)
  }

  def initialize(instructions)
    @instructions = instructions
    @origin = Orientation.new(0, 0, 0)
  end

  def follow_instructions
    @instructions.map { |instruction| instruction.split(" ") }
      .reduce(@origin) { |position, (direction, value)|
        MOTIONS[direction].new_orientation(position, value.to_i)
      }
  end
end

final_position = DiveMovement.new(DATA.readlines.map(&:chomp)).follow_instructions

pp final_position
pp final_position.horiz * final_position.depth

